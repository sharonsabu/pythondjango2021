# here we use method of "func with parameter and no return value"

# add pgm
def add(num1,num2):
    res=num1+num2
    print(res)
add(100,200)

#sub pgm
def sub(num1,num2):
    res=num1-num2
    print(res)
sub(200,100)

#multiplication pgm
def mul(num1,num2):
    res=num1*num2
    print(res)
mul(2,3)